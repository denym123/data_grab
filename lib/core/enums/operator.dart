enum Operator {
  greaterThan, // Representa o operador '>'
  greaterThanOrEqual, // Representa o operador '>='
  lessThan, // Representa o operador '<'
  lessThanOrEqual, // Representa o operador '<='
  equal, // Representa o operador '='
  notEqual, // Representa o operador '!='
  contains, // Representa o operador '@>'
  containedIn, // Representa o operador '<@>'
  notContains, // Representa o operador '!@>'
  and, // Representa o operador 'AND'
  or, // Representa o operador 'OR'
}
